    public static void mostrarDadosTabela(JTable tabela){
         String[] colunas = new String []{"Orientador", "Titulo", "Codigo", "Data de Inicio", "Data do Fim"};  
         String[][] dados = new String[0][0];
         DefaultTableModel dtm = new DefaultTableModel(dados, colunas);  
         int i = 0;
                 
         tabela.setModel(dtm);
         
        for(ProjetoPesquisa ppg: DadosProjetoPesquisa.getProjetoPesquisa())
        {
             Object []a = {ppg.getProfessorResposnsavel(), ppg.getTitulo(), ppg.getCodigo(), ppg.getDataInicio(), ppg.getDataTermino()};
             dtm.insertRow(i, a);
             i++;
        }    
         
    }
       
