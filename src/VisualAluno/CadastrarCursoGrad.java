    private void cadCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadCursoActionPerformed
        c = new CursoGraduacao();

        c.setNome(this.nomeCurso.getText());
        while (true)
        {
            try
            {
                c.setCodigo(Integer.parseInt(codCurso.getText()));
                this.i = 1;
                break;
            }catch (NumberFormatException e)
            {
                JOptionPane.showMessageDialog (null, "Codigo precisa receber um numero inteiro!");
                this.i=0;
                codCurso.setText("");
                break;
            }
        }
        while (true)
        {
            try
            {
                c.setDuracao(Integer.parseInt(duracaoCurso.getText()));
                this.i++;
                break;
            }catch (NumberFormatException e)
            {
                JOptionPane.showMessageDialog (null, "Duração precisa receber um numero inteiro!");
                this.i = 0;
                duracaoCurso.setText("");
                break;
            }
        }

        if(this.i == 2){
            DadosCursoGraduacao.cadastrarCursoCG(this.c);
            JOptionPane.showMessageDialog(null, "Curso Graduação Cadastrado");
        }
        else
        JOptionPane.showMessageDialog(null, "Corrija os dados!!");

        
    }//GEN-LAST:event_cadCursoActionPerformed
 
