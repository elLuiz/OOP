     public static void mostrarTabela(JTable x){
         String[] colunas = new String []{"Nome","Codigo","Duracao"};  
         String[][] dados = new String[0][0];
         DefaultTableModel dtm = new DefaultTableModel(dados, colunas);  
         int i = 0;
                 
         x.setModel(dtm);
         
        for(CursoPosGraduacao cpg: DadosCursoPosGraduacao.getCursoCPG())
        {
             Object []a = {cpg.getNome(), cpg.getCursoCod(), cpg.getDuracao()};
             dtm.insertRow(i, a);
             i++;
        }    
    }
    
